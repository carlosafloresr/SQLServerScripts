-- AP TRANSACTON
SELECT	HDR.VENDORID,
		HDR.DOCNUMBR,
		HDR.DOCDATE,
		HDR.DOCNUMBR,
		GL5.ACTNUMST,
		DET.DEBITAMT,
		DET.CRDTAMNT,
		DET.DistRef
FROM	PM30200 HDR
		INNER JOIN PM30600 DET ON HDR.VENDORID = DET.VENDORID AND HDR.VCHRNMBR = DET.VCHRNMBR
		INNER JOIN GL00105 GL5 ON DET.DSTINDX = GL5.ACTINDX
WHERE	HDR.DOCNUMBR = '9010188'

-- GL TRANSACTION
SELECT	GL2.JRNENTRY,
		GL2.REFRENCE,
		CAST(GL2.TRXDATE AS Date) AS TRXDATE,
		GL5.ACTNUMST,
		GL2.DEBITAMT,
		GL2.CRDTAMNT,
		CAST(GL2.ORPSTDDT AS Date) AS POSTDATE,
		'OPEN' AS Source
FROM	GL20000 GL2
		INNER JOIN GL00105 GL5 ON GL2.ACTINDX = GL5.ACTINDX
WHERE	GL2.REFRENCE IN ('96-107702|2042','96-107370|4862','96-107498|4865','96-107504|4868','96-107548|4872','96-107625|4879')
UNION
SELECT	GL1.JRNENTRY,
		GL1.DSCRIPTN,
		CAST(GL0.TRXDATE AS Date) AS TRXDATE,
		GL5.ACTNUMST,
		GL1.DEBITAMT,
		GL1.CRDTAMNT,
		NULL,
		'WORK' AS Source
FROM	GL10001 GL1
		INNER JOIN GL10000 GL0 ON GL1.JRNENTRY = GL0.JRNENTRY
		INNER JOIN GL00105 GL5 ON GL1.ACTINDX = GL5.ACTINDX
WHERE	GL0.REFRENCE IN ('96-107702|2042','96-107370|4862','96-107498|4865','96-107504|4868','96-107548|4872','96-107625|4879')

