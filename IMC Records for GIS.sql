SELECT	CustNmbr, CustName, CustClas, CASE WHEN Inactive = 1 THEN 'Inactive' ELSE 'Active' END AS Status, Hold FROM RM00101 
WHERE	CustNmbr IN (
					SELECT CustNmbr FROM RM10101 WHERE DstIndx IN (SELECT ActIndx FROM GL00105 WHERE ActNumSt IN ('0-00-1050'))
					UNION
					SELECT CustNmbr FROM RM30301 WHERE DstIndx IN (SELECT ActIndx FROM GL00105 WHERE ActNumSt IN ('0-00-1050')))
		AND Inactive = 0
		AND CustNmbr NOT IN (SELECT CustNmbr FROM GIS.dbo.RM00101)
ORDER BY CustName

SELECT VendorId, VendName, VndClsId, CASE WHEN VendStts <> 1 THEN 'Inactive' ELSE 'Active' END AS Status, Hold FROM PM00200 WHERE VendorId IN (
SELECT DISTINCT VendorId FROM PM10100 WHERE DstIndx IN (SELECT ActIndx FROM GL00105 WHERE ActNumSt IN ('0-00-2000','0-00-2050','0-00-2070'))
UNION
SELECT DISTINCT VendorId FROM PM30600 WHERE DstIndx IN (SELECT ActIndx FROM GL00105 WHERE ActNumSt IN ('0-00-2000','0-00-2050','0-00-2070'))) 
AND PM00200.VendorId NOT IN (SELECT VendorId FROM GIS.dbo.PM00200)
AND PM00200.VendStts = 1
AND PM00200.VndClsId <> 'DRV'
ORDER BY VendName

