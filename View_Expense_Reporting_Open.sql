		ISNULL(RTRIM(CAST(VEND1.VendorId AS Char(10))) + '-' + VEND1.VENDSHNM, 'General Ledger') AS Vendor,
		ISNULL(PM30200.DOCDATE, GL20000.TRXDATE) AS DOCDATE,
		PM30200.VOIDPDATE,
		GL20000.JRNENTRY,
		ISNULL(PM30200.VOIDED, GL20000.VOIDED) AS VOIDED,
		PV.ProNumber,
		UPPER(PV.TrailerNumber) AS TrailerNumber,
		UPPER(PV.ChassisNumber) AS ChassisNumber,
		CASE WHEN PM30600.DISTREF = '' OR PM30600.DISTREF IS Null THEN GL20000.SEQNUMBR ELSE PM30600.DSTSQNUM END AS SEQNUMBR,
		'GL20000.DSCRIPTN' AS SourceTable,
		ISNULL(PM30600.VchrNmbr, CAST(GL20000.JRNENTRY AS Char(10))) AS VchrNmbr,
		PM30600.DistType,
		PM30600.VendorId
		,PV.DriverId
FROM 	GL20000 
		INNER JOIN GL00100 ON GL20000.ACTINDX = GL00100.ACTINDX --ORGNTSRC
		LEFT JOIN PM30600 ON GL20000.OrcTrnum = PM30600.VchrNmbr AND GL20000.ORTRXSRC = PM30600.TRXSORCE AND GL20000.ORIGSEQNUM = PM30600.DSTSQNUM AND LEFT(GL20000.SOURCDOC, 2) = 'PM'
		LEFT JOIN PM30200 ON PM30600.VchrNmbr = PM30200.VchrNmbr AND PM30600.TRXSORCE = PM30200.TRXSORCE AND PM30200.VOIDED = 0
		LEFT JOIN GPCustom.dbo.Purchasing_Vouchers PV ON CASE WHEN LEFT(PM30600.TRXSORCE, 2) = 'PM' THEN PM30600.VchrNmbr ELSE CAST(GL20000.JRNENTRY AS Varchar(12)) END = PV.VoucherNumber AND CASE WHEN LEFT(GL20000.SOURCDOC, 2) = 'PM' THEN 'AP' ELSE 'GL' END = PV.Source AND PV.CompanyId = DB_NAME() --AND PM30600.DistType = 6
		LEFT JOIN PM00200 VEND1 ON PM30200.VendorId = VEND1.VendorId
WHERE 	GL20000.Voided = 0 AND
		((LEFT(GL20000.SOURCDOC, 2) = 'PM' AND PM30600.VchrNmbr IS NOT Null) OR
		(LEFT(GL20000.ORTRXSRC, 5) IN ('GLTRX','GLREV','CMTRX','CMADJ') AND LEFT(GL20000.SOURCDOC, 2) <> 'PM'))
UNION
