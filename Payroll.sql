SELECT	UPR30300.PAYROLCD, 
		UPR30300.UPRTRXAM, 
		UPR30300.CHEKDATE, 
		UPR00100.LASTNAME, 
		UPR00100.FRSTNAME, 
		UPR00100.MIDLNAME, 
		UPR00100.SOCSCNUM, 
		BE020230.CARRIER, 
		UPR00100.DEPRTMNT
FROM	IMC.dbo.UPR30300 UPR30300 
		INNER JOIN IMC.dbo.UPR00100 UPR00100 ON UPR30300.EMPLOYID=UPR00100.EMPLOYID
		INNER JOIN IMC.dbo.BE020230 BE020230 ON UPR30300.PAYROLCD=BE020230.BENEFIT
WHERE	UPR30300.CHEKDATE>={ts '2008-02-07 00:00:00'} AND UPR30300.CHEKDATE<{ts '2008-02-29 00:00:00'} AND 
		UPR30300.PAYROLCD>='401%' AND 
		UPR30300.PAYROLCD<='VAC' AND 
		BE020230.CARRIER='The Hartford Company'
ORDER BY 
		UPR00100.LASTNAME, 
		UPR00100.SOCSCNUM, 
		UPR30300.PAYROLCD


select	BE020230.CARRIER,
		UPR30300.*
from	UPR30300
		INNER JOIN IMC.dbo.BE020230 BE020230 ON UPR30300.PAYROLCD = BE020230.BENEFIT
where	employid in ('9142', '41', '574', '8570') and 
		CHEKDATE>={ts '2008-02-07 00:00:00'} AND 
		CHEKDATE<{ts '2008-02-29 00:00:00'} AND
		UPR30300.PAYROLCD>='401%' AND 
		UPR30300.PAYROLCD<='VAC' AND
		PYRLRTYP IN (2,3)
order by employid,payrolcd