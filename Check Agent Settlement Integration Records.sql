SELECT	HDR.TRXDSCRN,
		HDR.VCHRNMBR,
		HDR.DOCAMNT,
		GLA.ACTNUMST,
		GLM.ACTDESCR,
		DET.CRDTAMNT,
		DET.DEBITAMT,
		DET.DISTTYPE,
		DET.DistRef 
FROM	PM30200 HDR
		INNER JOIN PM30600 DET ON HDR.VENDORID = DET.VENDORID AND HDR.VCHRNMBR = DET.VCHRNMBR
		INNER JOIN GL00105 GLA ON DET.DSTINDX = GLA.ACTINDX
		INNER JOIN GL00100 GLM ON DET.DSTINDX = GLM.ACTINDX
WHERE	HDR.VENDORID = '1021' 
		AND HDR.DOCNUMBR = 'WE031718'

