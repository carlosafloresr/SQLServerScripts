SELECT	RTRIM(GL05.ACTNUMST) AS GLAccount,
		GL20.JRNENTRY AS JOURNAL,
		RTRIM(GL20.REFRENCE) AS [DESCRIPTION],
		CAST(GL20.TRXDATE AS Date) AS [DATE],
		GL20.PERIODID AS [MONTH],
		DATENAME(month, DATEADD(month, GL20.PERIODID, -1)) + '-' + CAST(GL20.OPENYEAR AS Varchar) AS [PERIOD],
		RTRIM(GL1.ACTDESCR) AS ACCT_Description,
		CAST(GL20.DEBITAMT AS Numeric(10,2)) AS DEBIT,
		CAST(GL20.CRDTAMNT AS Numeric(10,2)) AS CREDIT,
		GL20.TRXSORCE AS DatSource,
		RTRIM(GL20.ORDOCNUM) AS DocumentNumber,
		GL20.ORGNTSRC AS BACHNUMB
FROM	GL20000 GL20
		LEFT JOIN GL00105 GL05 ON GL20.ACTINDX = GL05.ACTINDX
		LEFT JOIN GL00100 GL1 on GL1.ACTINDX = GL05.ACTINDX
WHERE	GL20.PERIODID IN (5,6,7)
		AND GL05.ACTNUMST IN 
('0-04-1010',
'0-04-1011',
'0-00-1050',
'5-70-1100',
'5-00-1200',
'5-00-1201',
'0-00-2070',
'5-08-4013',
'5-08-4014',
'5-08-4015',
'5-08-4016',
'5-08-4017',
'5-08-4018',
'5-08-4043',
'5-08-4045',
'5-08-4046',
'5-08-4048',
'5-08-4050',
'5-08-5000',
'5-08-5001',
'5-08-5005',
'5-08-5007')
ORDER BY
		GL05.ACTNUMST,
		GL20.PERIODID,
		GL20.TRXDATE

