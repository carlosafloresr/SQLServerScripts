CREATE VIEW View_User_Companies	
AS
SELECT 	SY60100.UserId,
	SY01400.UserName,
	SY01400.UsrClass,
	SY60100.CmpanyId,
	SY01500.InterId AS DynDatabase,
	SY01500.CmpnyNam AS Company
FROM 	SY60100
	INNER JOIN SY01400 ON SY60100.UserId = SY01400.UserId
	INNER JOIN SY01500 ON SY60100.CmpanyId = SY01500.CmpanyId