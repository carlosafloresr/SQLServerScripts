		,CASE WHEN UN.InActive = 1 THEN 'INACTIVE' ELSE 'ACTIVE' END AS Status  --k
		,'TRACTOR'  --l
		,CASE WHEN [Manufacturer] LIKE 'INT%' THEN 'INT'
			WHEN [Manufacturer] LIKE 'MAC%' THEN 'MACKX'
			WHEN [Manufacturer] LIKE 'KENW%' THEN 'KNWRT'
			WHEN [Manufacturer] LIKE 'FR%' OR [Manufacturer] LIKE 'FEIG%' OR [Manufacturer] LIKE 'FRI%' THEN 'FRGHT'
